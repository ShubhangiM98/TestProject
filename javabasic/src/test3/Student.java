package test3;

public class Student {

	private int roll;
	private String name;
	private String dept;

	public Student(int roll, String name, String dept) {
		this.roll = roll;
		this.name = name;
		this.dept = dept;
	}

	public int getroll() {
		return roll;
	}

	public String getName() {
		return name;
	}

	public String getdept() {
		return dept;
	}

}
